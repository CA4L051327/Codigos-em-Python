n1 = input('Digite qualquer coisa?')
print('O tipo primitivo é:', type(n1))
print('Tem espaços?', n1.isspace())
print('É um numero?', n1.isnumeric())
print('É alfabetico?', n1.isalpha())
print('É alfa numerico?', n1.isalnum())
print('Esta em maisuculo?', n1.isupper())
print('Esta em minusculo?', n1.islower())
print('Esta capitalizado?', n1.istitle())


